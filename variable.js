// static variable

const x = 50;
console.log(x);

//the const can not be Redeclare

if (true) {
    const b = 100;
}
//we can call the b variable in outside it can't work

console.log(b);

//----------------------------------------------------------------------------------------------------------------->
//var
//----------------------------------------------------------------------------------------------------------------->

//Dynamic varialble it is a globle varialble

var a = 10;
console.log(a);

if (true) {
    var b = 100;
}
//we can call the b variable in outside it can be work

console.log(b);

// the var can be Redeclare

var a = 20;
console.log(a);

//----------------------------------------------------------------------------------------------------------------->
//let
//----------------------------------------------------------------------------------------------------------------->

//Dynamic variable

let c = 30;
console.log(c);

if (true) {
    let b = 100;
}
//we can call the b variable in outside it can be work

console.log(b);

//the let variable is can not be Redeclare

